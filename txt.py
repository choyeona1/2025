import streamlit as st

# 제목
st.title("🏃‍♂️ 스포츠 부상 위험 예측기")

st.write("운동 종목, 시간, 강도를 입력하면 예상되는 부상 위험도를 알려드립니다!")

# 운동 종목 선택
sport = st.selectbox("운동 종목을 선택하세요", 
                     ["축구 ⚽", "농구 🏀", "배드민턴 🏸", "조깅 🏃", "헬스 🏋️‍♂️", "요가 🧘"])

# 운동 시간 입력
hours = st.slider("주당 운동 시간 (시간)", 0, 20, 3)

# 운동 강도 선택
intensity = st.radio("운동 강도", ["낮음", "보통", "높음"])

# -------------------------------
# 위험 점수 계산 로직 (간단 버전)
# -------------------------------
base_risk = {
    "축구 ⚽": 40,
    "농구 🏀": 35,
    "배드민턴 🏸": 25,
    "조깅 🏃": 20,
    "헬스 🏋️‍♂️": 30,
    "요가 🧘": 10
}

risk = base_risk[sport]

# 시간에 따른 위험 증가
risk += hours * 2  

# 강도에 따른 가중치
if intensity == "보통":
    risk *= 1.2
elif intensity == "높음":
    risk *= 1.5

# 점수 제한
risk = min(risk, 100)

# -------------------------------
# 결과 출력
# -------------------------------
st.subheader("📊 예측 결과")
st.metric(label="부상 위험 점수 (0~100)", value=int(risk))

# 위험 등급 표시
if risk < 30:
    st.success("✅ 낮은 위험: 안심하고 운동할 수 있어요!")
elif risk < 60:
    st.warning("⚠️ 중간 위험: 준비운동/스트레칭 꼭 하세요!")
else:
    st.error("🚨 높은 위험: 무리하지 않도록 주의하세요!")

